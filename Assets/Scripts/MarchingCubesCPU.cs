    // Get the density value of the terrain at a given point
    float Density(Vector3 p)
    {
        float d = 0;

        // Simple density function to return a flat plane
        d = -p.y;

        // Add noise octaves
        d += Mathf.PerlinNoise(perlinSeed + p.x * 0.02f, perlinSeed + p.z * 0.02f) * 12;
        d += Mathf.PerlinNoise(perlinSeed + p.x * 0.04f, perlinSeed + p.z * 0.04f) * 6;
        d += Mathf.PerlinNoise(perlinSeed + p.x * 0.08f, perlinSeed + p.z * 0.08f) * 3;
        d += Mathf.PerlinNoise(perlinSeed + p.x * 0.16f, perlinSeed + p.z * 0.16f) * 1.5f;

        return d;
    }

    // Get the intersection point of the terrain between p1 and p2
    Vector3 InterpolateDensity (Vector3 p1, Vector3 p2, float d1, float d2)
    {
        if (d1 == d2)
            return p1;
        else
            return Vector3.Lerp(p1, p2, ((-d1) / (d2 - d1)));
    }

    // Calculate the normal vector on the surface at point p
    Vector3 CalculateNormal(Vector3 p)
    {

        Vector3 normal = new Vector3();

        normal = Vector3.up;

        Vector3 north = new Vector3(p.x, Density(new Vector3(p.x, 0.0f, p.z + 0.2f)), p.z + 0.2f) - p;
        Vector3 east = new Vector3(p.x + 0.2f, Density(new Vector3(p.x + 0.2f, 0.0f, p.z)), p.z) - p;
        Vector3 south = new Vector3(p.x, Density(new Vector3(p.x, 0.0f, p.z - 0.2f)), p.z - 0.2f) - p;
        Vector3 west = new Vector3(p.x - 0.2f, Density(new Vector3(p.x - 0.2f, 0.0f, p.z)), p.z) - p;

        Vector3 normalNE = Vector3.Cross(north, east);
        Vector3 normalES = Vector3.Cross(east, south);
        Vector3 normalSW = Vector3.Cross(south, west);
        Vector3 normalWN = Vector3.Cross(west, north);

        normal = (normalNE + normalES + normalSW + normalWN) * 0.25f;

        return normal.normalized;

    }

