    public void MarchingCubes()
    {

        perlinSeed = Random.Range(-10.0f, 10.0f);

        // For each voxel
        for (int x = 0; x < VoxelGridSizeX; x++)
        {
            for (int y = 0; y < VoxelGridSizeY; y++)
            {
                for (int z = 0; z < VoxelGridSizeZ; z++)
                {

                    // Get the position of each voxel vertex
                    Vector3[] gridPoints = new Vector3[8];
                    gridPoints[0] = new Vector3(x * VoxelScale,         y * VoxelScale,         z * VoxelScale);
                    gridPoints[1] = new Vector3((x + 1) * VoxelScale,   y * VoxelScale,         z * VoxelScale);
                    gridPoints[2] = new Vector3((x + 1) * VoxelScale,   (y + 1) * VoxelScale,   z * VoxelScale);
                    gridPoints[3] = new Vector3(x * VoxelScale,         (y + 1) * VoxelScale,   z * VoxelScale);
                    gridPoints[4] = new Vector3(x * VoxelScale,         y * VoxelScale,         (z + 1) * VoxelScale);
                    gridPoints[5] = new Vector3((x + 1) * VoxelScale,   y * VoxelScale,         (z + 1) * VoxelScale);
                    gridPoints[6] = new Vector3((x + 1) * VoxelScale,   (y + 1) * VoxelScale,   (z + 1) * VoxelScale);
                    gridPoints[7] = new Vector3(x * VoxelScale,         (y + 1) * VoxelScale,   (z + 1) * VoxelScale);

                    // Get density at each voxel vertex
                    float[] gridDensity = new float[8];
                    for (int i = 0; i < 8; i++)
                    {
                        gridDensity[i] = Density(gridPoints[i]);
                    }

                    // Get value from edgeTable
                    // This value shows which edges intersect the terrain surface
                    int tableIndex = 0;
                    if (gridDensity[0] < 0) tableIndex |= 1;
                    if (gridDensity[1] < 0) tableIndex |= 2;
                    if (gridDensity[2] < 0) tableIndex |= 4;
                    if (gridDensity[3] < 0) tableIndex |= 8;
                    if (gridDensity[4] < 0) tableIndex |= 16;
                    if (gridDensity[5] < 0) tableIndex |= 32;
                    if (gridDensity[6] < 0) tableIndex |= 64;
                    if (gridDensity[7] < 0) tableIndex |= 128;
                    int edgeTableValue = edgeTable[tableIndex];

                    // If all grid points are on one side of the surface, there are no polygons in this voxel
                    // So nothing is rendered
                    if (edgeTableValue == 0)
                        continue;

                    // Store the point of intersection on each voxel edge
                    Vector3[] intersectionPoints = new Vector3[12];

                    // For any edges intersected, find the intersection point
                    if ((edgeTableValue & 1) != 0)
                        intersectionPoints[0] = InterpolateDensity(gridPoints[0], gridPoints[1], gridDensity[0], gridDensity[1]);
                    if ((edgeTableValue & 2) != 0)
                        intersectionPoints[1] = InterpolateDensity(gridPoints[1], gridPoints[2], gridDensity[1], gridDensity[2]);
                    if ((edgeTableValue & 4) != 0)
                        intersectionPoints[2] = InterpolateDensity(gridPoints[2], gridPoints[3], gridDensity[2], gridDensity[3]);
                    if ((edgeTableValue & 8) != 0)
                        intersectionPoints[3] = InterpolateDensity(gridPoints[3], gridPoints[0], gridDensity[3], gridDensity[0]);
                    if ((edgeTableValue & 16) != 0)
                        intersectionPoints[4] = InterpolateDensity(gridPoints[4], gridPoints[5], gridDensity[4], gridDensity[5]);
                    if ((edgeTableValue & 32) != 0)
                        intersectionPoints[5] = InterpolateDensity(gridPoints[5], gridPoints[6], gridDensity[5], gridDensity[6]);
                    if ((edgeTableValue & 64) != 0)
                        intersectionPoints[6] = InterpolateDensity(gridPoints[6], gridPoints[7], gridDensity[6], gridDensity[7]);
                    if ((edgeTableValue & 128) != 0)
                        intersectionPoints[7] = InterpolateDensity(gridPoints[7], gridPoints[4], gridDensity[7], gridDensity[4]);
                    if ((edgeTableValue & 256) != 0)
                        intersectionPoints[8] = InterpolateDensity(gridPoints[0], gridPoints[4], gridDensity[0], gridDensity[4]);
                    if ((edgeTableValue & 512) != 0)
                        intersectionPoints[9] = InterpolateDensity(gridPoints[1], gridPoints[5], gridDensity[1], gridDensity[5]);
                    if ((edgeTableValue & 1024) != 0)
                        intersectionPoints[10] = InterpolateDensity(gridPoints[2], gridPoints[6], gridDensity[2], gridDensity[6]);
                    if ((edgeTableValue & 2048) != 0)
                        intersectionPoints[11] = InterpolateDensity(gridPoints[3], gridPoints[7], gridDensity[3], gridDensity[7]);

                    // Add intersection points to the vertex list in the order determined by triTable
                    for (int i = 0; i < 16; i++)
                    {
                        if (triTable[tableIndex, i] == -1)
                            break;
                        computedVertices.Add(intersectionPoints[triTable[tableIndex, i]]);

                        // Also add normals
                        computedNormals.Add(CalculateNormal(intersectionPoints[triTable[tableIndex, i]]));
                    }
                
                }
            }
        }

    }

    public void CreateMesh()
    {

        Mesh mesh = new Mesh();
        GetComponent<MeshFilter>().mesh = mesh;

        mesh.indexFormat = UnityEngine.Rendering.IndexFormat.UInt32;

        mesh.vertices = computedVertices.ToArray();
        mesh.normals = computedNormals.ToArray();

        int[] triangles = new int[computedVertices.Count];
        for (int i = 0; i < computedVertices.Count; i++)
        {
            triangles[i] = i;
        }
        mesh.triangles = triangles;

    }

    public void ClearArrays()
    {
        computedVertices = new List<Vector3>();
        computedNormals = new List<Vector3>();
    }

